package cn.yongjie.pojo;

public class BitVariable {

    private String shortName;

    private String category;

    private Integer bitPosition;

    private Integer numberOfBits;

    private String swCalibrationAccess;

    private String swClassRef;

    private String swVariableRef;

    private String reservedCol01;

    private String reservedCol02;

    private String reservedCol03;

    public String getShortName() {
        return shortName;
    }

    public void setShortName(String shortName) {
        this.shortName = shortName;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public Integer getBitPosition() {
        return bitPosition;
    }

    public void setBitPosition(Integer bitPosition) {
        this.bitPosition = bitPosition;
    }

    public Integer getNumberOfBits() {
        return numberOfBits;
    }

    public void setNumberOfBits(Integer numberOfBits) {
        this.numberOfBits = numberOfBits;
    }

    public String getSwCalibrationAccess() {
        return swCalibrationAccess;
    }

    public void setSwCalibrationAccess(String swCalibrationAccess) {
        this.swCalibrationAccess = swCalibrationAccess;
    }

    public String getSwClassRef() {
        return swClassRef;
    }

    public void setSwClassRef(String swClassRef) {
        this.swClassRef = swClassRef;
    }

    public String getSwVariableRef() {
        return swVariableRef;
    }

    public void setSwVariableRef(String swVariableRef) {
        this.swVariableRef = swVariableRef;
    }

    public String getReservedCol01() {
        return reservedCol01;
    }

    public void setReservedCol01(String reservedCol01) {
        this.reservedCol01 = reservedCol01;
    }

    public String getReservedCol02() {
        return reservedCol02;
    }

    public void setReservedCol02(String reservedCol02) {
        this.reservedCol02 = reservedCol02;
    }

    public String getReservedCol03() {
        return reservedCol03;
    }

    public void setReservedCol03(String reservedCol03) {
        this.reservedCol03 = reservedCol03;
    }
}
